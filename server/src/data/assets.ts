export const buildAssets = (): Asset[] =>
  mockedAssets
    .map((asset) => {
      const scores = calculateScoresForAsset(asset);

      return {
        ...asset,
        scores,
        statusLabel: getStatusLabel(scores),
        focusTags: getFocusTags(scores, asset.type),
        rank: 0,
      };
    })
    .sort((a, b) => b.scores.final - a.scores.final)
    .map((asset, index) => ({
      ...asset,
      rank: index + 1,
    }));

export const assets = buildAssets();
